#include "3-calc.h"
#include <stdio.h>
#include <stdlib.h>
/**
 * op_add - adds.
 * @a: 1 number.
 * @b: 2 number.
 *
 * Return: the result.
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - substraction.
 * @a: 1 number.
 * @b: 2 number.
 *
 * Return: the result.
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - multiplies.
 * @a: 1 number.
 * @b: 2 number.
 *
 * Return: the result.
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - divides.
 * @a: 1 number.
 * @b: 2 number.
 *
 * Return: the result.
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - modules..
 * @a: 1 number.
 * @b: 2 number.
 *
 * Return: the result.
 */
int op_mod(int a, int b)
{
	return (a % b);
}

